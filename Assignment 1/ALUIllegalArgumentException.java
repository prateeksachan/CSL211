public class ALUIllegalArgumentException extends IllegalArgumentException {
    public ALUIllegalArgumentException() {
        super();
    }

    public ALUIllegalArgumentException(String s) {
        super(s);
    }

    public ALUIllegalArgumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public ALUIllegalArgumentException(Throwable cause) {
        super(cause);
    }
}
